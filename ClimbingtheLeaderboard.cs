class Result
{

    /*link: https://www.hackerrank.com/challenges/climbing-the-leaderboard/problem
     * Complete the 'climbingLeaderboard' function below.
     *
     * The function is expected to return an INTEGER_ARRAY.
     * The function accepts following parameters:
     *  1. INTEGER_ARRAY ranked
     *  2. INTEGER_ARRAY player
     */

    public static List<int> climbingLeaderboard(List<int> ranked, List<int> player)
    {
        var newScores = new List<int>();
        var lastScore = int.MinValue;
        foreach(var score in ranked) 
        {
            if(score == lastScore)
                continue;
            newScores.Insert(0, score);
            lastScore = score;
        }

        var results = new List<int>();
        foreach(var aScore in player) {
            var binSearch = newScores.BinarySearch(aScore);

            if(binSearch >= 0)
            {
                results.Add(newScores.Count - binSearch);
            } else
            {
                binSearch = ~binSearch;
                results.Add(newScores.Count - binSearch + 1);
            }
        }
        return results;
    }

}

class Solution
{
    public static void Main(string[] args)
    {
        TextWriter textWriter = new StreamWriter(@System.Environment.GetEnvironmentVariable("OUTPUT_PATH"), true);

        int rankedCount = Convert.ToInt32(Console.ReadLine().Trim());

        List<int> ranked = Console.ReadLine().TrimEnd().Split(' ').ToList().Select(rankedTemp => Convert.ToInt32(rankedTemp)).ToList();

        int playerCount = Convert.ToInt32(Console.ReadLine().Trim());

        List<int> player = Console.ReadLine().TrimEnd().Split(' ').ToList().Select(playerTemp => Convert.ToInt32(playerTemp)).ToList();

        List<int> result = Result.climbingLeaderboard(ranked, player);

        textWriter.WriteLine(String.Join("\n", result));

        textWriter.Flush();
        textWriter.Close();
    }
}
